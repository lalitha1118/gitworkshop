# Custom python functions

def double_number(a):
    print(f'value before double_number(): {a}')
    y=a+a
    print(f'value after double_number(): {y}')
    return y

def square_number(a):
    y=a*a
    print(f'value before square_number(): {a}')
    print(f'value after square_number(): {y}')
    return y
