# Custom python functions

def double_number(a):
    """
    Function to double a number

    Parameters
    ----------
    a: int
        number to be doubled

    Returns
    ----------
    int
        doubled value of the given number
    """
    return a+a

def square_number(a):
    """
    Function to square a number

    Parameters
    ----------
    a: int
        number to be squared

    Returns
    ----------
    int
        squared value of the given number
    """
    return a*a
